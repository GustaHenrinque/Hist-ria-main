        <section class="section absolutismo">
            <h3>Absolutismo Monárquico</h3>
            <div class="content">
                <div class="text">
                    <p>O absolutismo foi um sistema de governo onde o poder era centralizado nas mãos do monarca, que detinha controle total sobre o estado. Os reis absolutistas, como Luís XIV na França, promoviam a ideia de que o poder era concedido por Deus, legitimando sua autoridade absoluta. Esse modelo de governo levou ao fortalecimento dos exércitos nacionais, à centralização fiscal e à diminuição do poder da nobreza local.</p>
                    <p>O absolutismo foi sustentado pelo mercantilismo, uma política econômica que incentivava a acumulação de riquezas e o monopólio do comércio exterior. Esse sistema contribuiu para o aumento das tensões sociais, que eventualmente culminaram nas revoluções burguesas do século XVIII, especialmente a Revolução Francesa.</p>
                </div>
