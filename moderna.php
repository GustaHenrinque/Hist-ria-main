            <div class="content">
                <div class="text">
                    <p>O Renascimento foi um movimento cultural e intelectual que valorizou o humanismo e a redescoberta da arte e filosofia clássicas. Esse movimento começou na Itália e se espalhou pela Europa, trazendo uma nova perspectiva sobre o homem, a ciência e o universo. Artistas como Leonardo da Vinci, Michelangelo e Rafael elevaram a pintura e escultura a novos níveis, enquanto cientistas como Copérnico e Galileu desafiaram conceitos medievais com novas teorias.</p>
                    <p>Além da arte, o Renascimento estimulou o pensamento crítico e a observação científica. A invenção da imprensa por Gutenberg foi um marco, pois facilitou a disseminação de ideias e o aumento do acesso ao conhecimento, ampliando o impacto desse movimento pela Europa.</p>
